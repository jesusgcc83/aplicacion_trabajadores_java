    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        this.lbInfo.setVisible(false);
        String idStr=jtID.getText();
        if("".equals(idStr)){
            mostrarMensaje("Debe ingresar un ID");
            return;
        }
        try {
            int id=Integer.parseInt(idStr);
            
            Conector_trabajadores con = new Conector_trabajadores();
            con.connect();
            ResultSet resultado = null;
            resultado=con.mostrarTrabajadores(id);
            if(resultado!=null){
                while (resultado.next()) {
                    this.jtID.setEditable(false);
                    jtNombre.setText(resultado.getString("nombre"));
                    jtDireccion.setText(resultado.getString("direccion"));
                    jtTelefono.setText(resultado.getString("telefono"));
                    jtSueldo.setText(Double.toString(resultado.getDouble("sueldo")));
                    
                    ComboBoxModel cbm=this.cbRegiones.getModel();
                    for (int i = 0; i < cbm.getSize(); i++) {
                        if(cbm.getElementAt(i).toString().equals(resultado.getString("region"))){
                            this.cbRegiones.setSelectedIndex(i);
                            return;
                        }
                    }
                    
                    
                }
            }
            con.close();
            limpiarCampos();
            this.jtID.setText(idStr);
            mostrarMensaje("ID no encontrado");
            
            
        } catch (Exception e) {
            mostrarMensaje("ID debe ser numerico");
        }
        
    }//GEN-LAST:event_btBuscarActionPerformed
    
    private void btBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBorrarActionPerformed
        
        this.lbInfo.setVisible(false);
        String idStr=jtID.getText();
        if("".equals(idStr)){
            mostrarMensaje("Debe ingresar un ID");
            return;
        }
        try {
            int id=Integer.parseInt(idStr);
            
            
            
                Conector_trabajadores con = new Conector_trabajadores();
                con.connect();
                ResultSet resultado = null;
                resultado=con.mostrarTrabajadores(id);
                
                if(resultado!=null)
                
                {
                    while (resultado.next()) {
                        if(resultado.getInt("id")==id){
                            con.deleteTrabajador(id);
                            mostrarMensaje("ID eliminado");
                            return;
                        }
                    
                    }
                                
                    
                }
                con.close();
                limpiarCampos();
                this.jtID.setText(idStr);
                mostrarMensaje("ID no encontrado");
            
        } catch (Exception e) {
            mostrarMensaje("ID debe ser numerico");
        }
    }//GEN-LAST:event_btBorrarActionPerformed
   
